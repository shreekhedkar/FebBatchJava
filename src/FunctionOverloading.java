
public class FunctionOverloading {

	public int addition(int a, int b) {

		return a + b;
	}

	public String addition(String a, String b) {

		return a + b;
	}

	public int addition(int a, int b, int c) {

		return a + b + c;
	}

	public int addition(int a, int b, int c, int d) {

		return a + b + c + d;
	}

}
